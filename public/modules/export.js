// modules/export.js
// Export library data as JSON (full backup) or PDF compendium (one chapter per trial).
// PDF compendium uses html2pdf.js — same library already loaded for the analyse tab.

import { getAccessToken } from './auth.js';
import { toast }          from '../components/toasts.js';

function _env(k,fb=''){return window.ENV?.[k]||fb;}
const getApiUrl=()=>_env('API_BASE_URL','https://app.rahmanmedical.co.uk/api');
const getApiToken=()=>_env('INTERNAL_API_TOKEN','');

// ─────────────────────────────────────────────
// JSON EXPORT
// Downloads the full library (or a filtered subset) as a .json file
// ─────────────────────────────────────────────
export async function exportJson(filters = {}) {
  try {
    toast.info('Preparing JSON export…');
    const trials = await _fetchAllTrials(filters);
    if (!trials.length) { toast.warning('No trials to export.'); return; }

    const payload = {
      exported_at: new Date().toISOString(),
      count:       trials.length,
      filters,
      trials,
    };

    const blob = new Blob([JSON.stringify(payload, null, 2)], { type: 'application/json' });
    _download(blob, `outcomelogic-library-${_dateSlug()}.json`);
    toast.success(`Exported ${trials.length} trial${trials.length !== 1 ? 's' : ''}.`);

  } catch (err) {
    toast.error(`Export failed: ${err.message}`);
  }
}

// ─────────────────────────────────────────────
// PDF COMPENDIUM EXPORT
// Renders each trial as an HTML chapter and converts to PDF
// ─────────────────────────────────────────────
export async function exportPdfCompendium(filters = {}, options = {}) {
  const { validatedOnly = true, title = 'OutcomeLogic — Landmark Trials Compendium' } = options;

  if (!window.html2pdf) {
    toast.error('PDF export library not loaded. Please refresh and try again.');
    return;
  }

  try {
    toast.info('Preparing compendium — this may take a moment…');

    const allFilters = { ...filters, validated_only: validatedOnly };
    const trials     = await _fetchAllTrials(allFilters);

    if (!trials.length) {
      toast.warning(validatedOnly
        ? 'No validated trials to export. Validate some trials first.'
        : 'No trials to export.');
      return;
    }

    // Build hidden render container
    const container = document.createElement('div');
    container.style.cssText = 'position:absolute;left:-9999px;top:0;width:800px;font-family:Georgia,serif;';
    document.body.appendChild(container);

    container.innerHTML = `
      <div style="padding:60px 60px 40px;page-break-after:always;">
        <h1 style="font-size:28px;font-weight:bold;color:#0f172a;margin-bottom:8px;">${escHtml(title)}</h1>
        <p style="font-size:14px;color:#64748b;">Generated by OutcomeLogic · rahmanmedical.co.uk</p>
        <p style="font-size:13px;color:#64748b;margin-top:4px;">${new Date().toLocaleDateString('en-GB', { day:'numeric',month:'long',year:'numeric' })}</p>
        <p style="font-size:13px;color:#64748b;margin-top:4px;">${trials.length} trial${trials.length !== 1 ? 's' : ''} · ${validatedOnly ? 'Validated entries only' : 'All entries'}</p>
        <div style="margin-top:40px;border-top:2px solid #0f172a;padding-top:20px;">
          <h2 style="font-size:16px;font-weight:bold;color:#0f172a;margin-bottom:12px;">Contents</h2>
          ${trials.map((t, i) => `
            <p style="font-size:13px;color:#334155;margin-bottom:4px;">
              ${i + 1}. ${escHtml(t.display_title)}
            </p>
          `).join('')}
        </div>
      </div>
      ${trials.map((t, i) => _chapterHtml(t, i + 1)).join('')}
    `;

    await window.html2pdf().set({
      margin:      [15, 15, 15, 15],
      filename:    `outcomelogic-compendium-${_dateSlug()}.pdf`,
      image:       { type: 'jpeg', quality: 0.95 },
      html2canvas: { scale: 1.5, scrollY: 0, useCORS: true },
      jsPDF:       { unit: 'mm', format: 'a4', orientation: 'portrait' },
      pagebreak:   { mode: ['css', 'legacy'] },
    }).from(container).save();

    document.body.removeChild(container);
    toast.success(`Compendium exported — ${trials.length} chapters.`);

  } catch (err) {
    toast.error(`Compendium export failed: ${err.message}`);
  }
}

